package Lesson1;

public class Working2 {
    public static void main (String[] args){
        int t = 5;
        int r = 17;

        boolean less = t + r < 10;
        boolean more = t + r >= 20;

        if (less) {
            System.out.println("false");
        } else if (more) {
            System.out.println("false");
        } else {
            System.out.println("true");
        }
    }
}
